/**
 * find_len - finds the length of a string
 * @str: string
 * Return: length of the string
 */

int find_len(char *str)
{
	int len = 0;

	while (*str++)
		len++;

	return (len);
}

/**
 * create_xarray - creates an array of chars
 * @size: size of array
 * Description: if there is insufficient space,
 * Return: pointer to the array
 */

char *create_xarray(int size)
{
	char *array;
	int index;

	array = malloc(sizeof(char) * size);

	if (array == NULL)
		exit(98);

	for (index = 0; index < (size - 1); index++)
		array[index] = 'x';
	array[index] = '\0';

	return (array);
}

/**
 * iterate_zeroes - iterates through a string of numbers
 * @str: the string of numbers to iterate through
 * Return: pointer to the next non zero element
 */

char *iterate_zeroes(char *str)
{
	while (*str && *str == '0')
		str++;

	return (str);
}

/**
 * get_digit - converts a digit character
 * @c: character to be converted
 * Description: if c is a non digit, the function exists
 * Return: converted int
 */

int get_digit(char c)
{
	int digit = c - '0';

	if (digit < 0 || digit > 9)
	{
		printf("Error\n");
		exit(98);
	}
	return (digit);
}

